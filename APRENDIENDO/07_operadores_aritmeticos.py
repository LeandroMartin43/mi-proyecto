print(9 + 5) #Devuelve la suma de dos numeros
print(9 - 5) #Devuelve la resta de dos numeros
print(9 * 5) #Devuelve el producto de dos numeros
print(9 / 5) #Devuelve el cociente de dos numeros
print(9 // 5) #Devuelve el cociente ENTERO de dos numeros
print(9 ** 5) #Devuelve la potencia entre dos numeros
print(9 % 5) #Devuelve el resto del cociente entre numeros